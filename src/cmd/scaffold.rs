#[cfg(test)]
mod tests {
    use std::path::PathBuf;

    use super::*;
    use crate::{
        ecosystem::{Destination, RouteDecision},
        model::{HarvestItem, ItemKind, SourceLang},
    };

    fn make_decision(kind: ItemKind, path: &str, dest: Destination) -> RouteDecision {
        RouteDecision {
            item: HarvestItem {
                rel_path: PathBuf::from(path),
                lang: SourceLang::Rust,
                kind,
                size_bytes: 100,
                notes: None,
            },
            destination: dest,
            rationale: "test".to_string(),
        }
    }

    #[test]
    fn gen_domain_logic_creates_file() {
        let tmp = tempfile::tempdir().unwrap();
        let decisions = vec![make_decision(
            ItemKind::DomainLogic,
            "src/order.rs",
            Destination::ExistingRepo {
                name: "doob".to_string(),
                url: "https://github.com/89jobrien/doob".to_string(),
            },
        )];

        // Write plan to temp file.
        let plan_file = tmp.path().join("plan.jsonl");
        let mut f = std::fs::File::create(&plan_file).unwrap();
        use std::io::Write as _;
        for d in &decisions {
            writeln!(f, "{}", serde_json::to_string(d).unwrap()).unwrap();
        }

        let out = tmp.path().join("output");
        run(Some(&plan_file), &out, false).unwrap();

        let stub = out.join("doob/src/domain/order.rs");
        assert!(stub.exists(), "domain stub should exist");
        let content = std::fs::read_to_string(&stub).unwrap();
        assert!(!content.is_empty());
        assert!(content.contains("Order"));
    }

    #[test]
    fn gen_port_creates_port_and_adapter() {
        let tmp = tempfile::tempdir().unwrap();
        let decisions = vec![make_decision(
            ItemKind::Port,
            "src/storage_port.rs",
            Destination::NewCrate {
                suggested_name: "storers".to_string(),
            },
        )];

        let plan_file = tmp.path().join("plan.jsonl");
        let mut f = std::fs::File::create(&plan_file).unwrap();
        use std::io::Write as _;
        for d in &decisions {
            writeln!(f, "{}", serde_json::to_string(d).unwrap()).unwrap();
        }

        let out = tmp.path().join("output");
        run(Some(&plan_file), &out, false).unwrap();

        assert!(out.join("storers/src/ports/storage_port.rs").exists());
        assert!(out.join("storers/src/adapters/storage_port_stub.rs").exists());
    }

    #[test]
    fn gen_conflict_aborts_without_force() {
        let tmp = tempfile::tempdir().unwrap();
        let decisions = vec![make_decision(
            ItemKind::DomainLogic,
            "src/order.rs",
            Destination::ExistingRepo {
                name: "doob".to_string(),
                url: "https://github.com/89jobrien/doob".to_string(),
            },
        )];

        let plan_file = tmp.path().join("plan.jsonl");
        let mut f = std::fs::File::create(&plan_file).unwrap();
        use std::io::Write as _;
        for d in &decisions {
            writeln!(f, "{}", serde_json::to_string(d).unwrap()).unwrap();
        }

        let out = tmp.path().join("output");
        run(Some(&plan_file), &out, false).unwrap();

        // Run again without force — should error.
        let result = run(Some(&plan_file), &out, false);
        assert!(result.is_err());
        assert!(result.unwrap_err().to_string().contains("conflicts"));
    }

    #[test]
    fn to_pascal_case_converts_correctly() {
        assert_eq!(to_pascal_case("order_service"), "OrderService");
        assert_eq!(to_pascal_case("storage"), "Storage");
        assert_eq!(to_pascal_case("my_domain_type"), "MyDomainType");
    }
}
